const kelvin = 0;
console.log(kelvin)
//the temperature in kelvin cannot be change
let celsius = kelvin - 273
console.log(celsius)
//convert K to c
let fahrenheit = celsius * (9/5) + 32
console.log(fahrenheit)
// converting c to F using the formula
roundF = Math.floor(fahrenheit)
console.log(roundF)
// round the number F to nearest integer
console.log(`The temperature is ${roundF} degree Fahrenheit.`)
